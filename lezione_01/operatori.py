#operatori
x: int=10 #cont int si dice esplicitamente che la variabile è intera 
y: int=5
print(x + y) #addizione
print(x - y) #sottrazione
print(x * y) #moltiplicazione
print(x / y) #divisione 
print(int (x / y)) #int converte un valore dato in un valore intero 
print(x ** y) #da una potenza x^y
print(x // y) #floor division, da la parte intera del risultato della divisione 
print(x % y) #da il resto della divisione
print(x == y) #x è uguale ad y ? si: True, no: False 
print(x != y) #x è diverso da y ?
print (x <= y) #x è minore o uguale a y ?







